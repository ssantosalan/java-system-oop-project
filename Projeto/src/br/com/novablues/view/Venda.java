    private void botaoAdicionarProdutoCaixaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAdicionarProdutoCaixaActionPerformed
        if (listaProduto.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(null, "Nenhum item selecionado!");

        } else if ("".equals(textoQuantidadeProduto.getText())) {
            JOptionPane.showMessageDialog(null, "Coloque a quantidade de produto!");
        }

        int estoqueAtual = Integer.parseInt(listaProduto.getValueAt(listaProduto.getSelectedRow(), 3).toString());
        int quantidade = Integer.parseInt(textoQuantidadeProduto.getText());

        if (quantidade > 0 && estoqueAtual > quantidade) {
            DefaultTableModel model = (DefaultTableModel) listaCarrinho.getModel();

            String id = listaProduto.getValueAt(listaProduto.getSelectedRow(), 0).toString();
            String descricao = listaProduto.getValueAt(listaProduto.getSelectedRow(), 1).toString();
            double preco = Double.parseDouble(listaProduto.getValueAt(listaProduto.getSelectedRow(), 2).toString().replace("R$", ""));
            String estoque = textoQuantidadeProduto.getText();

            listaProduto.setValueAt(String.valueOf(estoqueAtual - quantidade), listaProduto.getSelectedRow(), 3);

            model.addRow(new String[]{id, descricao, String.valueOf(preco), estoque});

            String total = labelTotal.getText().replace("R$", "");
            double totalD = Double.parseDouble(total);

            labelTotal.setText("R$" + (totalD + preco * quantidade));
        } else {
            JOptionPane.showMessageDialog(null, "Produto/Quantidade insuficiente em estoque!");
        }

    }//GEN-LAST:event_botaoAdicionarProdutoCaixaActionPerformed

