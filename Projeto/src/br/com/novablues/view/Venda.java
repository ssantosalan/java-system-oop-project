    private void botaoFinalizarVendaCaixaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoFinalizarVendaCaixaActionPerformed

        DefaultTableModel model = (DefaultTableModel) listaCarrinho.getModel();
        DefaultTableModel model2 = (DefaultTableModel) listaProduto.getModel();

        if (listaCarrinho.getRowCount() <= 0) {
            JOptionPane.showMessageDialog(null, "Não é possível finalizar a venda sem produtos!");
            return;
        }
        String cliente = labelCliente.getText();
        String vendedor = labelVendedor.getText();
        if (cliente.equalsIgnoreCase("Sem cliente")) {
            JOptionPane.showMessageDialog(null, "Não é possivel finalizar sem cliente. Favor inserir!");
            return;
        }
        if (vendedor.equalsIgnoreCase("Sem Vendedor")) {
            JOptionPane.showMessageDialog(null, "Não é possivel finalizar sem vendedor. Favor inserir!");
            return;
        }

        String sql = "Select max(numero_pedido + 1) FROM pedido_de_venda;";

        try {
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery();

            if (rs.next()) {
                String numero_pedido = rs.getString(1);

                int qtdLinhasTabela = model.getRowCount();
                for (int i = 0; i < qtdLinhasTabela; i++) {
                    String codigo = model.getValueAt(i, 0).toString();
                    String descricao = model.getValueAt(i, 1).toString();
                    String preco = model.getValueAt(i, 2).toString();
                    String quantidadevenda = model.getValueAt(i, 3).toString();

                    String att = "UPDATE estoque SET quantidade = quantidade -" + quantidadevenda + " WHERE nome_produto='" + descricao + "';";
                    String minhasql = "insert into pedido_de_venda (numero_pedido,quantidade_comprada,descricao_prod,fk_id_produto,nome_cliente,nome_vendedor,data_venda,preco_unit) "
                            + "values (" + numero_pedido + "," + quantidadevenda + ",'" + descricao + "'," + codigo + ",'" + labelCliente.getText() + "','" + labelVendedor.getText() + "','" + jDate.getText() + "','" + preco + "');";

                    st.executeUpdate(att);
                    st.executeUpdate(minhasql);

                }
                JOptionPane.showMessageDialog(null, "Venda realizada com sucesso!");
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro ao finalizar compra!");
        }

        model.setRowCount(0);
        model2.setRowCount(0);
        labelTotal.setText("R$ 0.00");
        labelCliente.setText("Sem Cliente");
        textoCPFVenda.setText("");
        textoQuantidadeProduto.setText("");

    }//GEN-LAST:event_botaoFinalizarVendaCaixaActionPerformed

