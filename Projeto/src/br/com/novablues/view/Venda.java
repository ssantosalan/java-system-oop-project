    private void botaoRemoverProdutoCaixa1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoRemoverProdutoCaixa1ActionPerformed

        int quantidadeVenda = Integer.parseInt(listaProduto.getValueAt(listaProduto.getSelectedRow(), 3).toString());;
        int quantidade = Integer.parseInt(listaCarrinho.getValueAt(listaCarrinho.getSelectedRow(), 3).toString());
        if (quantidadeVenda > 0) {
            DefaultTableModel model = (DefaultTableModel) listaCarrinho.getModel();
            double preco = Double.parseDouble(listaCarrinho.getValueAt(listaCarrinho.getSelectedRow(), 2).toString().replace("R$", ""));
            model.removeRow(listaCarrinho.getSelectedRow());
            String total = labelTotal.getText().replace("R$", "");
            double totalD = Double.parseDouble(total);

            labelTotal.setText("R$" + (totalD - preco * quantidade));
        } else {
            JOptionPane.showMessageDialog(null, "Lista errada");
        }
        listaProduto.setValueAt((quantidadeVenda + quantidade), listaProduto.getSelectedRow(), 3);

    }//GEN-LAST:event_botaoRemoverProdutoCaixa1ActionPerformed

